public class DisplayASCIITable {

	public static void main(String[] args) 
	{

		for(char ch = '!'; ch >= 33 && ch <= 126; ch++)
		{
				System.out.print(ch + " ");
				
				if (ch == 42)
						System.out.println();
				else if (ch == 52)
					System.out.println();
				else if (ch == 62)
					System.out.println();
				else if (ch == 72)
					System.out.println();
				else if (ch == 82)
					System.out.println();
				else if (ch == 92)
					System.out.println();
				else if (ch == 102)
					System.out.println();
				else if (ch == 112)
					System.out.println();
				else if (ch == 122)
					System.out.println();
				
						
		}
		
		
		
	
		
		
		
		
	}

}
